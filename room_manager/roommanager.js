function roundNum(n) {
    let units = n.toString().length;
    let remains = 0;
    if (units % 3 == 0) remains = (units / 3 - 1) * 3;
    else remains = units - (units % 3);
    let a = Math.trunc(n / 10 ** remains) * 10 ** remains;
    return a;
}

